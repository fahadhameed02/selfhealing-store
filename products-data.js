// دالة للحصول على منتج بواسطة ID
function getProductById(id) {
    return products.find(product => product.id === parseInt(id));
}

// دالة للحصول على المنتجات حسب الفئة
function getProductsByCategory(category) {
    if (category === 'الكل' || !category) {
        return products;
    }
    return products.filter(product => product.category === category);
}

// دالة للبحث في المنتجات
function searchProducts(searchTerm) {
    if (!searchTerm) return products;
    
    const term = searchTerm.toLowerCase();
    return products.filter(product => 
        product.name.toLowerCase().includes(term) ||
        product.description.toLowerCase().includes(term) ||
        product.details.toLowerCase().includes(term) ||
        product.category.toLowerCase().includes(term) ||
        product.benefits.some(benefit => benefit.toLowerCase().includes(term))
    );
}

// دالة للفلترة حسب السعر
function filterByPrice(minPrice, maxPrice) {
    return products.filter(product => 
        product.price >= minPrice && product.price <= maxPrice
    );
}

// دالة للفلترة حسب التقييم
function filterByRating(minRating) {
    return products.filter(product => product.rating >= minRating);
}

